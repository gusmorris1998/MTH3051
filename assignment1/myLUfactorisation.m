%--INPUTS------------------------------------------------------------------
%
% A          -     invertible square matrix
%
%--OUTPUT------------------------------------------------------------------
%
% A          -     LU decomposition [L,U] of A, stored within A 
%
%--------------------------------------------------------------------------

%--------------------------------------------------------------------------
% DO NOT MODIFY THE FUNCTION DEFINITION!
%--------------------------------------------------------------------------

function A=myLUfactorisation(A)
    d=size(A,1);
    for k = 1:d-1
        for i = k + 1:d
            A(i,k) = A(i,k)/A(k,k);
            for j = k + 1:d
                A(i,j) = A(i,j) - A(i,k)*A(k,j);
            end
        end
    end
