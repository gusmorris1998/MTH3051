% This is determinant computation using Laplace expansion from
% Proposition 2.19. This task is a bit tricky.
%--------------------------------------------------------------------------
% 
%--INPUTS------------------------------------------------------------------
%
% A          -     square matrix
%
%--OUTPUT------------------------------------------------------------------
%
% s          -     determinant of A
%
%--------------------------------------------------------------------------

%--------------------------------------------------------------------------
% DO NOT MODIFY THE FUNCTION DEFINITION!
%--------------------------------------------------------------------------

function s=myLaplaceExpansion(A)
    s = 0;
    d = size(A,1);
    if (d == 1)
        s = A(1);
    else
        for i = 1:d
            ACross = A;
            ACross(i, :) = [];
            ACross(:, 1) = [];
            s = s + ((-1)^(i + 1)) * A(i,1) * myLaplaceExpansion(ACross);
        end
    end


