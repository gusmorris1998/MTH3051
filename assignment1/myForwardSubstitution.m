% This is memory-efficient forward substitution as in Algorithm 2.26
%--------------------------------------------------------------------------
%
%--INPUTS------------------------------------------------------------------
%
% L          -     lower triangular matrix
% b          -     column vector
%
%--OUTPUT------------------------------------------------------------------
%
% b          -     solution x of of Lx=b, stored in column vector b
%
%--------------------------------------------------------------------------

%--------------------------------------------------------------------------
% DO NOT MODIFY THE FUNCTION DEFINITION!
%--------------------------------------------------------------------------

function b=myForwardSubstitution(L,b)
    d=size(L,1);
    for i = 1:d
        for j = 1:i-1
            b(i) = b(i) - L(i, j) * b(j);
        end
        b(i) = b(i)/L(i, i);
    end

