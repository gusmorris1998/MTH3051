% This is memory-efficient backward substitution, similar to Algorithm 2.26
%--------------------------------------------------------------------------
%
%--INPUTS------------------------------------------------------------------
%
% U          -     upper triangular matrix
% b          -     column vector
%
%--OUTPUT------------------------------------------------------------------
%
% b          -     solution x of Ux=b, stored in column vector b
%
%--------------------------------------------------------------------------

%--------------------------------------------------------------------------
% DO NOT MODIFY THE FUNCTION DEFINITION!
%--------------------------------------------------------------------------

function b=myBackwardSubstitution(U,b)
    d=size(U,1);
    for i = d:-1:1
        for j = d: -1: i+1
            b(i) = b(i) - U(i, j) * b(j);
        end
        b(i) = b(i)/U(i, i);
    end

