% This is the Simpson rule as in Example 7.8
%----------------------------------------------------------------
%
%--INPUTS--------------------------------------------------------
%
% f          -     function handle
% a          -     real number
% b          -     real number with a<b
%
%--OUTPUT--------------------------------------------------------
%
% s          -     approximation of \int_a^b f(x) dx
%
%----------------------------------------------------------------

%----------------------------------------------------------------
% DO NOT MODIFY THE FUNCTION DEFINITION!
%----------------------------------------------------------------

function s=mySimpson(f,a,b)
    s = ((b-a)/6)*(f(a) + 4*f((a+b)/2) + f(b));

