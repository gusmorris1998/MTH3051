%--INPUTS--------------------------------------------------------
%
% f          -     function handle
% a          -     real number
% b          -     real number with a<b
%
%--OUTPUT--------------------------------------------------------
%
% s          -     approximation of \int_a^b f(x) dx
%
%----------------------------------------------------------------

%----------------------------------------------------------------
% DO NOT MODIFY THE FUNCTION DEFINITION!
%----------------------------------------------------------------

function s=myTrapezoidal(f,a,b)
    s = ((b-a)/2)*(f(b) + f(a));

