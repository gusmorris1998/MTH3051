% This is the composite Simpson rule as in Example 7.15
%----------------------------------------------------------------
%
%--INPUTS--------------------------------------------------------
%
% f          -     function handle
% a          -     real number
% b          -     real number with a<b
% N          -     number of subintervals
%
%--OUTPUT--------------------------------------------------------
%
% s          -     approximation of \int_a^b f(x) dx
%
%----------------------------------------------------------------

%----------------------------------------------------------------
% DO NOT MODIFY THE FUNCTION DEFINITION!
%----------------------------------------------------------------

function s=myCompSimpson(f,a,b,N)
    h = (b-a)/N;

    s = f(a);
    for i=1:N-1
         s = s + 2 * f(a+i*h);
    end

    s = s + f(b); 
    for i=0:N-1
        s = s + 4 * f(a+(i+1/2)*h);
    end

    s = (h/6) * s;

